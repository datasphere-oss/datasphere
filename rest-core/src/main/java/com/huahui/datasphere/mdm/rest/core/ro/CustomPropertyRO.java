package com.huahui.datasphere.mdm.rest.core.ro;

import java.io.Serializable;

public class CustomPropertyRO implements Serializable {
    /**
     * GSVUID.
     */
    private static final long serialVersionUID = 8803217337371359810L;
    /**
     * Custom property name.
     */
    private String name;
    /**
     * Custom property value.
     */
    private String value;

    public CustomPropertyRO() {
    }

    public CustomPropertyRO(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
